class Matematik {
  void cikarmaIslami(int sayi1, int sayi2) {
    int cikart = sayi1 - sayi2;
    print("sayilarin farki : $cikart");
  }

  void carpmaIslami(int sayi1, int sayi2) {
    int carp = sayi1 * sayi2;
    print("sayilarin carpimi : $carp");
  }

  void bolmeIslemi(int sayi1, int sayi2) {
    double bol = sayi1 / sayi2;
    print("sayilarin farki : $bol");
  }
}
